import {
  detectAlbums,
  isAutoGeneratedAlbum,
  matchSidecarToMedia,
  parseGoogleTakeoutSidecar,
  type TakeoutMediaItem,
} from '$lib/utils/google-takeout-parser';

describe('parseGoogleTakeoutSidecar', () => {
  it('should parse a complete sidecar with all fields', () => {
    const json = JSON.stringify({
      title: 'IMG_1234.jpg',
      description: 'A beautiful sunset',
      photoTakenTime: { timestamp: '1609459200' },
      geoData: { latitude: 48.8566, longitude: 2.3522, altitude: 35 },
      favorited: true,
      archived: true,
    });

    const result = parseGoogleTakeoutSidecar(json);

    expect(result).not.toBeNull();
    expect(result!.title).toBe('IMG_1234.jpg');
    expect(result!.description).toBe('A beautiful sunset');
    expect(result!.dateTaken).toEqual(new Date(1_609_459_200_000));
    expect(result!.latitude).toBe(48.8566);
    expect(result!.longitude).toBe(2.3522);
    expect(result!.isFavorite).toBe(true);
    expect(result!.isArchived).toBe(true);
  });

  it('should return null for non-Takeout JSON', () => {
    const json = JSON.stringify({ name: 'not a takeout file', size: 1234 });
    expect(parseGoogleTakeoutSidecar(json)).toBeNull();
  });

  it('should return null for malformed JSON', () => {
    expect(parseGoogleTakeoutSidecar('{not valid json}')).toBeNull();
  });

  it('should return null for empty string', () => {
    expect(parseGoogleTakeoutSidecar('')).toBeNull();
  });

  it('should handle missing optional fields', () => {
    const json = JSON.stringify({
      title: 'IMG_5678.jpg',
      photoTakenTime: { timestamp: '1609459200' },
    });

    const result = parseGoogleTakeoutSidecar(json);

    expect(result).not.toBeNull();
    expect(result!.title).toBe('IMG_5678.jpg');
    expect(result!.description).toBeUndefined();
    expect(result!.dateTaken).toEqual(new Date(1_609_459_200_000));
    expect(result!.latitude).toBeUndefined();
    expect(result!.longitude).toBeUndefined();
    expect(result!.isFavorite).toBe(false);
    expect(result!.isArchived).toBe(false);
  });

  it('should filter GPS (0,0) as no-location', () => {
    const json = JSON.stringify({
      title: 'IMG_9999.jpg',
      photoTakenTime: { timestamp: '1609459200' },
      geoData: { latitude: 0, longitude: 0 },
    });

    const result = parseGoogleTakeoutSidecar(json);

    expect(result).not.toBeNull();
    expect(result!.latitude).toBeUndefined();
    expect(result!.longitude).toBeUndefined();
  });

  it('should fall back to creationTime when photoTakenTime is missing', () => {
    const json = JSON.stringify({
      title: 'IMG_0001.jpg',
      creationTime: { timestamp: '1609545600' },
    });

    const result = parseGoogleTakeoutSidecar(json);

    expect(result).not.toBeNull();
    expect(result!.dateTaken).toEqual(new Date(1_609_545_600_000));
  });

  it('should fall back to geoDataExif when geoData is missing', () => {
    const json = JSON.stringify({
      title: 'IMG_0002.jpg',
      photoTakenTime: { timestamp: '1609459200' },
      geoDataExif: { latitude: 40.7128, longitude: -74.006 },
    });

    const result = parseGoogleTakeoutSidecar(json);

    expect(result).not.toBeNull();
    expect(result!.latitude).toBe(40.7128);
    expect(result!.longitude).toBe(-74.006);
  });

  it('should filter invalid GPS ranges', () => {
    const json = JSON.stringify({
      title: 'IMG_0003.jpg',
      photoTakenTime: { timestamp: '1609459200' },
      geoData: { latitude: 100, longitude: 200 },
    });

    const result = parseGoogleTakeoutSidecar(json);

    expect(result).not.toBeNull();
    expect(result!.latitude).toBeUndefined();
    expect(result!.longitude).toBeUndefined();
  });

  it('should trim whitespace-only descriptions to undefined', () => {
    const json = JSON.stringify({
      title: 'IMG_0004.jpg',
      description: '   \n\t  ',
      photoTakenTime: { timestamp: '1609459200' },
    });

    const result = parseGoogleTakeoutSidecar(json);

    expect(result).not.toBeNull();
    expect(result!.description).toBeUndefined();
  });
});

describe('matchSidecarToMedia', () => {
  const validSidecar = JSON.stringify({
    title: 'IMG_1234.jpg',
    photoTakenTime: { timestamp: '1609459200' },
  });

  it('should match sidecar to media by exact filename', () => {
    const result = matchSidecarToMedia('Takeout/Google Photos/Trip/IMG_1234.jpg.json', validSidecar, [
      'Takeout/Google Photos/Trip/IMG_1234.jpg',
      'Takeout/Google Photos/Trip/IMG_5678.jpg',
    ]);
    expect(result).toBe('Takeout/Google Photos/Trip/IMG_1234.jpg');
  });

  it('should return undefined when no media matches', () => {
    const result = matchSidecarToMedia('Takeout/Google Photos/Trip/IMG_9999.jpg.json', validSidecar, [
      'Takeout/Google Photos/Trip/IMG_1234.jpg',
    ]);
    expect(result).toBeUndefined();
  });

  it('should match truncated filenames (47-char Google limit)', () => {
    // Google truncates long filenames to 47 characters before appending .json
    const longName = 'this_is_a_really_long_filename_that_exceeds_47_chars_total.jpg';
    const truncated = longName.slice(0, 47);
    const result = matchSidecarToMedia(`Takeout/Google Photos/Album/${truncated}.json`, validSidecar, [
      `Takeout/Google Photos/Album/${longName}`,
    ]);
    expect(result).toBe(`Takeout/Google Photos/Album/${longName}`);
  });

  it('should return undefined for non-Takeout JSON sidecar', () => {
    const nonTakeout = JSON.stringify({ name: 'not takeout' });
    const result = matchSidecarToMedia('Takeout/Google Photos/Trip/IMG_1234.jpg.json', nonTakeout, [
      'Takeout/Google Photos/Trip/IMG_1234.jpg',
    ]);
    expect(result).toBeUndefined();
  });
});

describe('isAutoGeneratedAlbum', () => {
  it('should detect "Photos from YYYY" as auto-generated', () => {
    expect(isAutoGeneratedAlbum('Photos from 2023')).toBe(true);
    expect(isAutoGeneratedAlbum('Photos from 2019')).toBe(true);
  });

  it('should not flag real album names', () => {
    expect(isAutoGeneratedAlbum('Summer Vacation')).toBe(false);
    expect(isAutoGeneratedAlbum('Family Photos')).toBe(false);
    expect(isAutoGeneratedAlbum('Photos from Paris')).toBe(false);
  });
});

describe('detectAlbums', () => {
  it('should extract albums from folder structure', () => {
    const items: TakeoutMediaItem[] = [
      { path: 'Takeout/Google Photos/Summer Trip/IMG_001.jpg', file: new File([], 'IMG_001.jpg') },
      { path: 'Takeout/Google Photos/Summer Trip/IMG_002.jpg', file: new File([], 'IMG_002.jpg') },
      { path: 'Takeout/Google Photos/Birthday/IMG_003.jpg', file: new File([], 'IMG_003.jpg') },
    ];

    const albums = detectAlbums(items);

    expect(albums).toHaveLength(2);
    const summerTrip = albums.find((a) => a.name === 'Summer Trip');
    expect(summerTrip).toBeDefined();
    expect(summerTrip!.itemCount).toBe(2);
    expect(summerTrip!.isAutoGenerated).toBe(false);
    expect(summerTrip!.itemPaths).toEqual([
      'Takeout/Google Photos/Summer Trip/IMG_001.jpg',
      'Takeout/Google Photos/Summer Trip/IMG_002.jpg',
    ]);

    const birthday = albums.find((a) => a.name === 'Birthday');
    expect(birthday).toBeDefined();
    expect(birthday!.itemCount).toBe(1);
  });

  it('should mark auto-generated albums', () => {
    const items: TakeoutMediaItem[] = [
      { path: 'Takeout/Google Photos/Photos from 2023/IMG_001.jpg', file: new File([], 'IMG_001.jpg') },
    ];

    const albums = detectAlbums(items);

    expect(albums).toHaveLength(1);
    expect(albums[0].name).toBe('Photos from 2023');
    expect(albums[0].isAutoGenerated).toBe(true);
  });

  it('should return empty array for flat files', () => {
    const items: TakeoutMediaItem[] = [{ path: 'IMG_001.jpg', file: new File([], 'IMG_001.jpg') }];

    const albums = detectAlbums(items);

    expect(albums).toHaveLength(0);
  });
});
