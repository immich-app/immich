export interface TakeoutMetadata {
  title: string;
  description: string | undefined;
  dateTaken: Date | undefined;
  latitude: number | undefined;
  longitude: number | undefined;
  isFavorite: boolean;
  isArchived: boolean;
}

export interface TakeoutMediaItem {
  path: string;
  file: File;
  metadata?: TakeoutMetadata;
  albumName?: string;
}

export interface TakeoutAlbum {
  name: string;
  itemCount: number;
  isAutoGenerated: boolean;
  itemPaths: string[];
}

function parseTimestamp(obj: { timestamp?: string } | undefined): Date | undefined {
  if (!obj?.timestamp) {
    return undefined;
  }
  const seconds = Number(obj.timestamp);
  if (Number.isNaN(seconds) || seconds <= 0) {
    return undefined;
  }
  return new Date(seconds * 1000);
}

function parseGps(
  geoData: { latitude?: number; longitude?: number } | undefined,
  geoDataExif: { latitude?: number; longitude?: number } | undefined,
): { latitude: number | undefined; longitude: number | undefined } {
  const geo = geoData ?? geoDataExif;
  if (!geo || geo.latitude === undefined || geo.longitude === undefined) {
    return { latitude: undefined, longitude: undefined };
  }

  const { latitude, longitude } = geo;

  // Filter (0,0) as no-location
  if (latitude === 0 && longitude === 0) {
    return { latitude: undefined, longitude: undefined };
  }

  // Filter invalid GPS ranges
  if (latitude < -90 || latitude > 90 || longitude < -180 || longitude > 180) {
    return { latitude: undefined, longitude: undefined };
  }

  // If primary source has (0,0) or invalid, try fallback
  if (geoData && geoDataExif && geoData !== geoDataExif) {
    const primary = geoData;
    if (
      (primary.latitude === 0 && primary.longitude === 0) ||
      primary.latitude === undefined ||
      primary.longitude === undefined
    ) {
      return parseGps(geoDataExif, undefined);
    }
  }

  return { latitude, longitude };
}

export function parseGoogleTakeoutSidecar(jsonString: string): TakeoutMetadata | null {
  if (!jsonString || jsonString.trim().length === 0) {
    return null;
  }

  let data: Record<string, unknown>;
  try {
    data = JSON.parse(jsonString);
  } catch {
    return null;
  }

  if (typeof data !== 'object' || data === null) {
    return null;
  }

  // Must have at least photoTakenTime or creationTime to be a Takeout sidecar
  const photoTakenTime = data.photoTakenTime as { timestamp?: string } | undefined;
  const creationTime = data.creationTime as { timestamp?: string } | undefined;

  if (!photoTakenTime && !creationTime) {
    return null;
  }

  const title = (data.title as string) || '';
  const rawDescription = data.description as string | undefined;
  const description = rawDescription?.trim() ? rawDescription.trim() : undefined;

  const dateTaken = parseTimestamp(photoTakenTime) ?? parseTimestamp(creationTime);

  const geoData = data.geoData as { latitude?: number; longitude?: number } | undefined;
  const geoDataExif = data.geoDataExif as { latitude?: number; longitude?: number } | undefined;
  const { latitude, longitude } = parseGps(geoData, geoDataExif);

  const isFavorite = data.favorited === true;
  const isArchived = data.archived === true;

  return {
    title,
    description,
    dateTaken,
    latitude,
    longitude,
    isFavorite,
    isArchived,
  };
}

/**
 * Match a JSON sidecar file to its corresponding media file.
 * Google Takeout places sidecars alongside media with `.json` appended (e.g. `IMG_1234.jpg.json`).
 * For filenames longer than 47 characters, Google truncates before appending `.json`.
 *
 * Returns the matching media path, or undefined if no match found or sidecar is not valid Takeout JSON.
 */
export function matchSidecarToMedia(
  sidecarPath: string,
  sidecarContent: string,
  mediaFilePaths: string[],
): string | undefined {
  // Validate that the sidecar is actually a Takeout JSON file
  if (!parseGoogleTakeoutSidecar(sidecarContent)) {
    return undefined;
  }

  // Strip .json extension to get the expected media path
  if (!sidecarPath.endsWith('.json')) {
    return undefined;
  }
  const expectedMediaPath = sidecarPath.slice(0, -5);

  // Try exact match first
  const exactMatch = mediaFilePaths.find((p) => p === expectedMediaPath);
  if (exactMatch) {
    return exactMatch;
  }

  // Fallback: truncated filename matching (Google's 47-char limit)
  const sidecarDir = expectedMediaPath.slice(0, Math.max(0, expectedMediaPath.lastIndexOf('/')));
  const sidecarBasename = expectedMediaPath.slice(Math.max(0, expectedMediaPath.lastIndexOf('/') + 1));

  if (sidecarBasename.length > 0) {
    const truncatedMatch = mediaFilePaths.find((p) => {
      const dir = p.slice(0, Math.max(0, p.lastIndexOf('/')));
      const basename = p.slice(Math.max(0, p.lastIndexOf('/') + 1));
      return dir === sidecarDir && basename.startsWith(sidecarBasename) && basename.length > sidecarBasename.length;
    });
    if (truncatedMatch) {
      return truncatedMatch;
    }
  }

  return undefined;
}

const AUTO_GENERATED_ALBUM_PATTERN = /^Photos from \d{4}$/;

/**
 * Returns true for auto-generated album names like "Photos from 2023".
 */
export function isAutoGeneratedAlbum(name: string): boolean {
  return AUTO_GENERATED_ALBUM_PATTERN.test(name);
}

/**
 * Extract album information from the folder structure of Takeout items.
 * Google Takeout organizes photos into folders like:
 *   Takeout/Google Photos/<AlbumName>/filename.jpg
 *
 * Items not in the expected folder structure are excluded from albums.
 */
export function detectAlbums(items: TakeoutMediaItem[]): TakeoutAlbum[] {
  const albumMap = new Map<string, string[]>();

  for (const item of items) {
    const parts = item.path.split('/');
    // Expected structure: Takeout/Google Photos/<AlbumName>/filename
    // We need at least 4 parts to have an album folder
    if (parts.length < 4) {
      continue;
    }

    // The album name is the second-to-last directory component
    const albumName = parts.at(-2);
    if (!albumName) {
      continue;
    }

    // Skip the root "Google Photos" folder itself — we want subfolders of it
    const googlePhotosIndex = parts.indexOf('Google Photos');
    if (googlePhotosIndex === -1 || googlePhotosIndex >= parts.length - 2) {
      continue;
    }

    const existing = albumMap.get(albumName);
    if (existing) {
      existing.push(item.path);
    } else {
      albumMap.set(albumName, [item.path]);
    }
  }

  return [...albumMap.entries()].map(([name, itemPaths]) => ({
    name,
    itemCount: itemPaths.length,
    isAutoGenerated: isAutoGeneratedAlbum(name),
    itemPaths,
  }));
}
